//! `.sextant/rules.lock` — pinned vendor packs + per-file SHA-256 hashes.
//! The lock is the source of truth for which packs are installed; the
//! loader refuses to grade if any vendor file has been modified, removed,
//! or added without a matching lock entry.

use std::collections::BTreeMap;
use std::path::{Path, PathBuf};

use serde::{Deserialize, Serialize};
use sha2::{Digest, Sha256};
use thiserror::Error;

pub const LOCK_FILENAME: &str = "rules.lock";
pub const VENDOR_DIR: &str = "vendor";
const LOCK_VERSION: u32 = 1;

/// Top-level on-disk schema. `version` lets us evolve the format without
/// silently misreading older locks.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
pub struct LockFile {
    pub version: u32,
    #[serde(default)]
    pub packs: Vec<LockedPack>,
}

#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
pub struct LockedPack {
    pub name: String,
    pub source: String,
    #[serde(default, rename = "ref")]
    pub reference: String,
    #[serde(default)]
    pub revision: String,
    #[serde(default, skip_serializing_if = "String::is_empty")]
    pub subdir: String,
    #[serde(default, skip_serializing_if = "String::is_empty")]
    pub fetched_at: String,
    /// `relative-path -> "sha256:<hex>"`. BTreeMap keeps the on-disk file
    /// order stable so diffs stay readable.
    #[serde(default)]
    pub files: BTreeMap<String, String>,
}

#[derive(Debug, Error)]
pub enum LockError {
    #[error("io ({path:?}): {source}")]
    Io {
        path: PathBuf,
        #[source]
        source: std::io::Error,
    },
    #[error("parsing {path:?}: {source}")]
    Parse {
        path: PathBuf,
        #[source]
        source: toml::de::Error,
    },
    #[error("serializing lock: {0}")]
    Serialize(#[from] toml::ser::Error),
    #[error(
        "rules.lock has version {found}, this sextant supports {supported}; \
         re-run `sextant rules update` after upgrading"
    )]
    UnsupportedVersion { found: u32, supported: u32 },
    #[error(
        "vendor pack `{pack}`: file `{file}` is missing from `.sextant/rules/vendor/{pack}/` \
         but is recorded in rules.lock — run `sextant rules update` or `sextant rules remove`"
    )]
    MissingFile { pack: String, file: String },
    #[error(
        "vendor pack `{pack}`: file `{file}` exists on disk but is not in rules.lock — \
         the pack has been tampered with; re-run `sextant rules add` or `sextant rules update`"
    )]
    UntrackedFile { pack: String, file: String },
    #[error(
        "vendor pack `{pack}`: file `{file}` has been modified (hash mismatch) — \
         restore the original contents or re-run `sextant rules update`"
    )]
    HashMismatch { pack: String, file: String },
    #[error("vendor pack `{pack}` directory is missing from .sextant/rules/vendor/")]
    MissingPackDir { pack: String },
}

pub type LockResult<T> = Result<T, LockError>;

impl LockFile {
    pub fn empty() -> Self {
        Self {
            version: LOCK_VERSION,
            packs: Vec::new(),
        }
    }

    /// Read `.sextant/rules.lock` from a repo root. Returns `Ok(None)` when
    /// the lock is absent — a repo without vendor packs is the common case.
    pub fn read(repo_root: &Path) -> LockResult<Option<Self>> {
        let path = lock_path(repo_root);
        if !path.exists() {
            return Ok(None);
        }
        let text = std::fs::read_to_string(&path).map_err(|source| LockError::Io {
            path: path.clone(),
            source,
        })?;
        let lock: LockFile = toml::from_str(&text).map_err(|source| LockError::Parse {
            path: path.clone(),
            source,
        })?;
        if lock.version != LOCK_VERSION {
            return Err(LockError::UnsupportedVersion {
                found: lock.version,
                supported: LOCK_VERSION,
            });
        }
        Ok(Some(lock))
    }

    pub fn write(&self, repo_root: &Path) -> LockResult<()> {
        let path = lock_path(repo_root);
        if let Some(parent) = path.parent() {
            std::fs::create_dir_all(parent).map_err(|source| LockError::Io {
                path: parent.to_path_buf(),
                source,
            })?;
        }
        let body = toml::to_string_pretty(self)?;
        let preface = "# Auto-generated by `sextant rules add` / `update`. Do not edit by hand —\n\
                       # this file's hashes gate vendor-pack integrity.\n\n";
        std::fs::write(&path, format!("{preface}{body}"))
            .map_err(|source| LockError::Io { path, source })?;
        Ok(())
    }

    pub fn upsert(&mut self, pack: LockedPack) {
        if let Some(existing) = self.packs.iter_mut().find(|p| p.name == pack.name) {
            *existing = pack;
        } else {
            self.packs.push(pack);
        }
        self.packs.sort_by(|a, b| a.name.cmp(&b.name));
    }

    pub fn remove(&mut self, pack_name: &str) -> bool {
        let len_before = self.packs.len();
        self.packs.retain(|p| p.name != pack_name);
        len_before != self.packs.len()
    }

    pub fn pack(&self, name: &str) -> Option<&LockedPack> {
        self.packs.iter().find(|p| p.name == name)
    }
}

/// Compute the SHA-256 of `bytes`, formatted as `sha256:<hex>` for storage.
pub fn hash_bytes(bytes: &[u8]) -> String {
    let mut h = Sha256::new();
    h.update(bytes);
    format!("sha256:{}", hex::encode(h.finalize()))
}

/// Compute hashes for every regular file under `dir`, keyed by the path
/// relative to `dir` with forward slashes.
pub fn hash_directory(dir: &Path) -> LockResult<BTreeMap<String, String>> {
    let mut out = BTreeMap::new();
    if !dir.exists() {
        return Ok(out);
    }
    for entry in walkdir_files(dir)? {
        let rel = entry
            .strip_prefix(dir)
            .expect("walkdir entries are descendants of dir")
            .to_string_lossy()
            .replace('\\', "/");
        let bytes = std::fs::read(&entry).map_err(|source| LockError::Io {
            path: entry.clone(),
            source,
        })?;
        out.insert(rel, hash_bytes(&bytes));
    }
    Ok(out)
}

/// Validate that the on-disk pack directory matches the locked hashes.
/// Catches: missing files, extra files, modified files.
pub fn verify_pack(pack: &LockedPack, pack_dir: &Path) -> LockResult<()> {
    if !pack_dir.exists() {
        return Err(LockError::MissingPackDir {
            pack: pack.name.clone(),
        });
    }
    let actual = hash_directory(pack_dir)?;
    for (rel, expected) in &pack.files {
        match actual.get(rel) {
            None => {
                return Err(LockError::MissingFile {
                    pack: pack.name.clone(),
                    file: rel.clone(),
                });
            }
            Some(got) if got != expected => {
                return Err(LockError::HashMismatch {
                    pack: pack.name.clone(),
                    file: rel.clone(),
                });
            }
            _ => {}
        }
    }
    for rel in actual.keys() {
        if !pack.files.contains_key(rel) {
            return Err(LockError::UntrackedFile {
                pack: pack.name.clone(),
                file: rel.clone(),
            });
        }
    }
    Ok(())
}

pub fn lock_path(repo_root: &Path) -> PathBuf {
    repo_root.join(".sextant").join(LOCK_FILENAME)
}

pub fn vendor_root(repo_root: &Path) -> PathBuf {
    repo_root.join(".sextant").join("rules").join(VENDOR_DIR)
}

pub fn pack_dir(repo_root: &Path, pack_name: &str) -> PathBuf {
    vendor_root(repo_root).join(pack_name)
}

fn walkdir_files(root: &Path) -> LockResult<Vec<PathBuf>> {
    let mut out = Vec::new();
    walkdir_files_into(root, &mut out)?;
    out.sort();
    Ok(out)
}

fn walkdir_files_into(dir: &Path, out: &mut Vec<PathBuf>) -> LockResult<()> {
    let entries = std::fs::read_dir(dir).map_err(|source| LockError::Io {
        path: dir.to_path_buf(),
        source,
    })?;
    for entry in entries {
        let entry = entry.map_err(|source| LockError::Io {
            path: dir.to_path_buf(),
            source,
        })?;
        let path = entry.path();
        let ft = entry.file_type().map_err(|source| LockError::Io {
            path: path.clone(),
            source,
        })?;
        if ft.is_dir() {
            walkdir_files_into(&path, out)?;
        } else if ft.is_file() {
            out.push(path);
        }
    }
    Ok(())
}

#[cfg(test)]
