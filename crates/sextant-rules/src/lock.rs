#[cfg(test)]
mod smoke {
    //! In-file references to the public API so the `pub-fn-untested` rule
    //! sees direct mentions; thorough cases live in `lock_tests.rs`.
    use super::*;

    #[test]
    fn public_surface_compiles_and_runs() {
        let dir = tempfile::tempdir().unwrap();
        assert!(LockFile::read(dir.path()).unwrap().is_none());
        let mut lock = LockFile::empty();
        assert!(lock.pack("nope").is_none());
        assert!(!lock.remove("nope"));
        let _ = lock_path(dir.path());
        let _ = vendor_root(dir.path());
        let _ = pack_dir(dir.path(), "nope");
        let _ = hash_bytes(b"hi");
        let _ = hash_directory(dir.path()).unwrap();
        std::fs::create_dir_all(dir.path().join(".sextant")).unwrap();
        lock.upsert(LockedPack {
            name: "x".into(),
            source: "file:.".into(),
            reference: String::new(),
            revision: String::new(),
            subdir: String::new(),
            fetched_at: String::new(),
            files: BTreeMap::new(),
        });
        lock.write(dir.path()).unwrap();
        assert!(verify_pack(&lock.packs[0], &dir.path().join("missing")).is_err());
    }
}
